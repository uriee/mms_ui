import moment from 'moment';
import { parse, stringify } from 'qs';

export function fixedZero(val) {
  return val * 1 < 10 ? `0${val}` : val;
}

/***
* getTimeDistance : Set's a predetermind Time Frame to the dashboard graphs
* @type : Time Farame
*/
export function getTimeDistance(type) {
  const now = new Date();
  const oneDay = 1000 * 60 * 60 * 24;

  if (type === 'today') {
    now.setHours(0);
    now.setMinutes(0);
    now.setSeconds(0);
    return [moment(now), moment(now.getTime() + (oneDay - 1000))];
  }

  if (type === 'week') {
    let day = now.getDay();
    now.setHours(0);
    now.setMinutes(0);
    now.setSeconds(0);

    const beginTime = now.getTime() - day * oneDay;

    return [moment(beginTime), moment(beginTime + (7 * oneDay - 1000))];
  }

  if (type === 'month') {
    const year = now.getFullYear();
    const month = now.getMonth();
    const nextDate = moment(now).add(1, 'months');
    const nextYear = nextDate.year();
    const nextMonth = nextDate.month();

    return [
      moment(`${year}-${fixedZero(month + 1)}-01 00:00:00`),
      moment(moment(`${nextYear}-${fixedZero(nextMonth + 1)}-01 00:00:00`).valueOf() - 1000),
    ];
  }

  const year = now.getFullYear();
  return [moment(`${year}-01-01 00:00:00`), moment(`${year}-12-31 23:59:59`)];
}


/***
* treefy : format recursivly the data from db to fit into UI cascaders
* @xs : data entries
* @keys :  the order of data cascading 
* @name : nedded for recursion never to be used (allways = 'root')
*/
export function treefy(xs, keys,name='root') {
  var ret = {}
  ret.label = ret.value = name
  ret.children = xs.map(x=> ({value : Object.values(x)[0],label : Object.values(x)[0]}))
  if (keys.length === 0 ) return  ret
  const [,...Keys] = keys
  const children =  xs.reduce(function(rv, x) {
      const y = {...x};
      delete y[keys[0]];
      (rv[x[keys[0]]] = rv[x[keys[0]]] || []).push(y);
      return rv
  }, {})   
  ret.children  =  Object.keys(children).map(x=> treefy(children[x],Keys,x)) 
  return ret
};

export function groupBy(xs, key) {
  return xs.reduce(function(rv, x) {
    (rv[x[key]] = rv[x[key]] || []).push(x);
    return rv;
  }, {});
};

export function getPageQuery() {
  return parse(window.location.href.split('?')[1]);
}

export function getQueryPath(path = '', query = {}) {
  const search = stringify(query);
  if (search.length) {
    return `${path}?${search}`;
  }
  return path;
}

